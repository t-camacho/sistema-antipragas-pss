    private boolean verificaCampos(){
        boolean cpf = false, descrip = false, orcamento = false;
        if(!txtCPF.getText().isEmpty()){
            cpf = true;
        }
        if(!txtDescrip.getText().isEmpty()){
            descrip= true;
        }
        if(!txtOrcamento.getText().isEmpty()){
            orcamento = true;
        }
        return cpf && descrip && orcamento;
    }
    
    private void limpaCampos(){
        txtCPF.setText("");
        txtDescrip.setText("");
        txtOrcamento.setText("");
    }

    private void inicializarCombo(){
        DefaultComboBoxModel dcm = new DefaultComboBoxModel();
        dcm.addElement("Esporádico");
        dcm.addElement("Periódico");
        cmbTipo.setModel(dcm);
    }
    
    private void atualizarTabelaProposta(){
        limparTabela();
        DAOProposta daoProposta = new DAOProposta();
        DefaultTableModel modelo = (DefaultTableModel) tblProposta.getModel();
        daoProposta.listar(modelo);
    }
    
    public void limparTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tblProposta.getModel();
        modelo.setNumRows(0);
        tblProposta.setModel(modelo);
    }
    
    private void PesquisarProposta(String param){
        DAOProposta daoProposta = new DAOProposta();
        DefaultTableModel modelo = (DefaultTableModel) tblProposta.getModel();
        daoProposta.listarComJoinCliente(param, BarraDeBusca.getText(), modelo);
    }
    
    private void setProposta(int idProposta){
        DAOProposta daoProposta = new DAOProposta();
        this.proposta = daoProposta.buscarComID(idProposta);

        txtCPF.setText(this.proposta.getCliente().getCadastro().getCpf());
        txtDescrip.setText(this.proposta.getDescricao());
        txtOrcamento.setText(String.valueOf(this.proposta.getOrcamento()));
        
        for(int i = 0; i<cmbTipo.getModel().getSize(); i++){
            if(cmbTipo.getModel().getElementAt(i).equals(this.proposta.getTipoServico())){
                cmbTipo.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void ativarBotoes(){
        this.btnDelete.setEnabled(true);
        this.btnMerge.setEnabled(true);
        this.btnSave.setEnabled(true);
        this.btnDeliberar.setEnabled(true);
        this.btnRecusar.setEnabled(true);
    }
    
    private void desativarBotoes(){
        this.btnDelete.setEnabled(false);
        this.btnMerge.setEnabled(false);
        this.btnSave.setEnabled(false);
        this.btnDeliberar.setEnabled(false);
        this.btnRecusar.setEnabled(false);
    }
    
    private void criarProposta(){
        char [] array;
        this.proposta = new Proposta();
        //proposta = new Proposta();
        DAOCliente daoCliente = new DAOCliente();
        try {
            proposta.setCliente((Cliente)daoCliente.buscarComCPF(txtCPF.getText()));
            proposta.setDescricao(txtDescrip.getText());
            proposta.setOrcamento(Float.parseFloat(txtOrcamento.getText()));
            array = ((String)cmbTipo.getSelectedItem()).toCharArray();
            if(array[0] == 'P'){
                proposta.setTipoServico("P");
            }else{
                proposta.setTipoServico("I");
            }
            proposta.setStatus("P");
        } catch (Exception e) {
        }
        
    }
    
