    private void btnMergeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMergeActionPerformed
        if(verificaCampos()){
            char [] array;
            //proposta = new Proposta();
            DAOCliente daoCliente = new DAOCliente();
            DAOProposta daoProposta = new DAOProposta();
            proposta.setCliente((Cliente)daoCliente.buscarComCPF(txtCPF.getText()));
            proposta.setDescricao(txtDescrip.getText());
            proposta.setOrcamento(Float.parseFloat(txtOrcamento.getText()));
            array = ((String)cmbTipo.getSelectedItem()).toCharArray();
            if(array[0] == 'P'){
                proposta.setTipoServico("P");
            }else{
                proposta.setTipoServico("I");
            }
            proposta.setStatus("P");
            daoProposta.alterar(this.proposta);
            
            atualizarTabelaProposta();
            limpaCampos();
            this.proposta = null;
            this.btnDelete.setEnabled(false);
            this.btnMerge.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null, "Campo(s) obrigatório(s) em branco.");
        }
    }//GEN-LAST:event_btnMergeActionPerformed

    private void tblPropostaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPropostaMousePressed
        int linhaTabelaProposta = tblProposta.getSelectedRow();
        this.proposta = new Proposta();
        String id = tblProposta.getModel().getValueAt(linhaTabelaProposta, 0).toString();
        setProposta(Integer.parseInt(id));
        this.proposta.setIdProposta(Integer.parseInt(id));
        
        this.ativarBotoes();
    }//GEN-LAST:event_tblPropostaMousePressed

