    private void tblUsuariosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMousePressed
        // TODO add your handling code here:
        int linhaTabelaUsuario = tblUsuarios.getSelectedRow();
        String id = tblUsuarios.getModel().getValueAt(linhaTabelaUsuario, 0).toString();
        setCliente(Integer.parseInt(id));
        this.ativarBotoes();
    }//GEN-LAST:event_tblUsuariosMousePressed

