    private void flush(){
        atualizarTabelaUsuario();
        this.desativarBotoes();
        this.btnSave.setEnabled(true);
        this.limpaCampos();
    }
    
    private void limpaCampos(){
        txtLogin.setText("");
        txtSenha.setText("");
    }
    
    private boolean verificaCampos(){
        boolean login = false, senha = false;
        if(!txtLogin.getText().isEmpty()){
            login = true;
        }
        if(!txtSenha.getText().isEmpty()){
            senha = true;
        }
        return login && senha;
    }
    
    private void setCliente(int idUsuario){
        DAOUsuario dAOUsuario = new DAOUsuario();
        this.usuario = dAOUsuario.buscarComID(idUsuario);
        
        txtLogin.setText(this.usuario.getLogin());
        txtSenha.setText(this.usuario.getSenha());
        cTipo.setSelectedItem(this.usuario.getTipo());
    }
    
    private void PesquisarCliente(){
        DAOUsuario dAOUsuario = new DAOUsuario();
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        dAOUsuario.listarComLike(BarraDeBusca.getText(), modelo);
    }
    
    private void atualizarTabelaUsuario()
    {
        limparTabela();
        DAOUsuario dAOusuario = new DAOUsuario();
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        dAOusuario.listar(modelo);
    }
    
    private void ativarBotoes(){
        this.btnDelete.setEnabled(true);
        this.btnMerge.setEnabled(true);
        this.btnSave.setEnabled(true);
    }
    
    private void desativarBotoes(){
        this.btnDelete.setEnabled(false);
        this.btnMerge.setEnabled(false);
        this.btnSave.setEnabled(false);
    }
    
    private Usuario criarUsuario(){
        Usuario u = new Usuario();
        
        u.setLogin(txtLogin.getText());
        u.setSenha(txtSenha.getText());
        u.setTipo(cTipo.getSelectedItem().toString());
        return u;
    }
    
  
    
    public void limparTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setNumRows(0);
        tblUsuarios.setModel(modelo);
    }
    
