    private void setFornecedor(int idFornecedor){
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        this.fornecedor = dAOFornecedor.buscarComID(idFornecedor);
        
        txtNome.setText(this.fornecedor.getNome());
        
        txtCelDD.setText(this.fornecedor.getTelefone().getTelCelular().substring(0, 2));
        txtCel.setText(this.fornecedor.getTelefone().getTelCelular().substring(2));
        txtFixoDD.setText(this.fornecedor.getTelefone().getTelFixo().substring(0, 2));
        txtFixo.setText(this.fornecedor.getTelefone().getTelFixo().substring(2));
        
        int idEnd = this.fornecedor.getEndereco().getIdEndereco();
        for(int i = 0; i < jEnd.getItemCount(); i++){
            StringTokenizer aux = new StringTokenizer(jEnd.getItemAt(i), " ");
            int idScroll = Integer.parseInt(aux.nextToken());
            if(idScroll == idEnd){
                jEnd.setSelectedIndex(i);
                break;
            }
        }
        
    }
    
    private void atualizarTabelaFornecedor()
    {
        limparTabela();
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        DefaultTableModel modelo = (DefaultTableModel) tblFornecedores.getModel();
        dAOFornecedor.listar(modelo);
    }
    
    
    public void limparTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tblFornecedores.getModel();
        modelo.setNumRows(0);
        tblFornecedores.setModel(modelo);
    }
    
    private void PesquisarFornecedores(){
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        DefaultTableModel modelo = (DefaultTableModel) tblFornecedores.getModel();
        dAOFornecedor.listarComLike(BarraDeBusca.getText(), modelo);
    }
    
    private Fornecedor criarFornecedor(){
        Fornecedor f = new Fornecedor();
        Endereco endereco = new Endereco();
        Telefone telefone = new Telefone();
        f.setNome(txtNome.getText());
        StringTokenizer aux = new StringTokenizer(jEnd.getSelectedItem().toString(), " ");
        endereco.buscarComId(Integer.parseInt(aux.nextToken()));
        
        String num = txtCelDD.getText()+txtCel.getText();
        telefone.setTelCelular(num);
        num = txtFixoDD.getText()+txtFixo.getText();
        telefone.setTelFixo(num);
        
        f.setEndereco(endereco);
        f.setTelefone(telefone);
        
        
        return f;
    }

