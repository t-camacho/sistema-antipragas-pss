    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        dAOFornecedor.excluir(this.fornecedor.getIdFornecedor());
        this.limparTabela();
        atualizarTabelaFornecedor();
        this.fornecedor = null;
        limpaCampos();
        this.btnDelete.setEnabled(false);
        this.btnMerge.setEnabled(false);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        if(verificaCampos()){
            Fornecedor f = criarFornecedor();
            dAOFornecedor.armazenar(f);
        }else{
            JOptionPane.showMessageDialog(null, "Campo(s) obrigatório(s) em branco.");
        }
        
        limpaCampos();
        this.fornecedor = null;
        this.btnDelete.setEnabled(false);
        this.btnMerge.setEnabled(false);
        atualizarTabelaFornecedor();
    }//GEN-LAST:event_btnSaveActionPerformed


