    private void btnMergeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMergeActionPerformed
        // TODO add your handling code here:
        DAOFornecedor dAOFornecedor = new DAOFornecedor();
        Fornecedor f = criarFornecedor();
        f.setIdFornecedor(this.fornecedor.getIdFornecedor());
        dAOFornecedor.alterar(f);
        
        limpaCampos();
        atualizarTabelaFornecedor();
        this.fornecedor = null;
        this.btnDelete.setEnabled(false);
        this.btnMerge.setEnabled(false);
    }//GEN-LAST:event_btnMergeActionPerformed

    private void tblFornecedoresMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFornecedoresMousePressed
        // TODO add your handling code here:
        int linhaTabelaForn = tblFornecedores.getSelectedRow();
        String id = tblFornecedores.getModel().getValueAt(linhaTabelaForn, 0).toString();
        this.fornecedor = new Fornecedor();
        this.fornecedor.setIdFornecedor(Integer.parseInt(id));
        setFornecedor(Integer.parseInt(id));
        this.ativarBotoes();
    }//GEN-LAST:event_tblFornecedoresMousePressed

